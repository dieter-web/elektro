/** SECTION Platte */
class vPlatte extends vBauelemente
{
  constructor ( data, point )
  {
    super( data, point );
  }
  fshape ()
  {
    this.Symbol.select( "#shape" )
      .append( "rect" )
      .attr( "x", 0 )
      .attr( "y", 0 )
      .attr( "width", 3 )
      .attr( "height", 365 )
      .attr( "fill", "none" )
      .attr( "stroke", "rgb(240,240,240" )
      .attr( "pointer-events", "all" )
      .attr( "transform", () =>
      {
        return `translate(${ this.point.x }, ${ this.point.y })`;
      } );
  }
}
